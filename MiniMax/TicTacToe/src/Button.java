import java.awt.*;
public class Button
{
    int x;
    int y;
    int width;
    int height;
    Color color;
    String text;
    public Button(int x, int y, int width, int height, Color color, String text)
    {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.color = color;
        this.text = text;
    }
}